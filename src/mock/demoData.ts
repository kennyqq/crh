function lerp(start: number, end: number, progress: number) {
  return start + (end - start) * progress
}

function interpolatePath(points: Coordinates[], progress: number): Coordinates {
  const safeProgress = Math.min(Math.max(progress, 0), 1)
  const maxIndex = points.length - 1

  if (maxIndex <= 0) {
    return points[0]
  }

  const scaled = safeProgress * maxIndex
  const index = Math.min(Math.floor(scaled), maxIndex - 1)
  const localProgress = scaled - index
  const current = points[index]
  const next = points[index + 1]

  return [
    Number(lerp(current[0], next[0], localProgress).toFixed(6)),
    Number(lerp(current[1], next[1], localProgress).toFixed(6)),
  ]
}

function buildTrainReplay(
  trainId: string,
  routeId: string,
  routePoints: Coordinates[],
  boardValues: number[],
  prsValues: number[],
  rsrpValues: number[],
  sinrValues: number[],
  upValues: number[],
  downValues: number[],
  highlightedFrames: Record<number, string>,
): TrainReplayFrame[] {
  return boardValues.map((boardExperience, index) => {
    const progress = index / Math.max(boardValues.length - 1, 1)
    return {
      timestampLabel: `15:${String(index * 5).padStart(2, '0')}`,
      trainId,
      routeId,
      position: interpolatePath(routePoints, progress),
      prsExperience: prsValues[index],
      boardExperience,
      rsrp: rsrpValues[index],
      sinr: sinrValues[index],
      uplinkMbps: upValues[index],
      downlinkMbps: downValues[index],
      status:
        boardExperience <= 68
          ? 'shock'
          : boardExperience <= 78
            ? 'handover'
            : index > 8
              ? 'recover'
              : 'stable',
      highlightSegmentId: highlightedFrames[index],
    }
  })
}

function buildComplaintReplay(
  routePoints: Coordinates[],
  centerProgress: number,
  severityBoost: number,
  prefers4g: boolean,
  alertOffset = 0,
): ComplaintReplayFrame[] {
  return Array.from({ length: 121 }, (_, index): ComplaintReplayFrame => {
    const offsetSeconds = index * 5 - 300
    const distance = Math.abs(offsetSeconds) / 300
    const dip = Math.exp(-Math.pow(offsetSeconds / 75, 2))
    const progress = Math.min(Math.max(centerProgress + offsetSeconds / 1500, 0), 1)
    const signalPenalty = dip * severityBoost
    const networkMode: ComplaintReplayFrame['networkMode'] =
      prefers4g && Math.abs(offsetSeconds - alertOffset) < 40 ? '4G' : '5G'

    return {
      offsetSeconds,
      position: interpolatePath(routePoints, progress),
      rsrp: Number((-92 - signalPenalty * 11 - distance * 2).toFixed(2)),
      sinr: Number((15.6 - signalPenalty * 6 - distance).toFixed(2)),
      networkMode,
      rtt: Number((58 + signalPenalty * 42).toFixed(1)),
      uplinkMbps: Number((2.8 - signalPenalty * 1.45 + distance * 0.4).toFixed(2)),
      downlinkMbps: Number((42 - signalPenalty * 18 + distance * 1.8).toFixed(2)),
      retransmission: Number((0.8 + signalPenalty * 5.4).toFixed(1)),
      lowSpeedRatio: Number((0.06 + signalPenalty * 0.36).toFixed(2)),
      alert:
        Math.abs(offsetSeconds - alertOffset) <= 5
          ? networkMode === '4G'
            ? '检测到 5G 不连续驻留并短时掉落 4G'
            : '检测到列车瞬时冲击，业务时延与重传同步上升'
          : undefined,
    }
  })
}

